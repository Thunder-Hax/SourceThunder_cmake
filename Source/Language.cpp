int power::loadallLangage()
{
    RAWTRANSLATION.clear();
    // Récupération du répertoire de travail de l'application
    string appPath = Directory::get_current_dir() + "\\ThunderMenu\\";
    wstring_convert<codecvt_utf8_utf16<wchar_t>> converter;
    wstring workingDir = converter.from_bytes(appPath);
    // Charger les données traduites depuis le fichier
    std::ifstream file;
    file.open(workingDir + L"langage.langage");
    if (file)
    {
    std::string currentLine;
    while (std::getline(file, currentLine)) {
        RAWTRANSLATION.push_back(currentLine);
    }

    // Charger les fonctions de la langue
    langage::loadfunction2();
    }
    file.close();

    return 0;
}